    private void BTLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTLoginActionPerformed
        try {
            String usuario, senha;
            usuario = getTxtUsuario().getText();
            senha = getTxtSenha().getText();
            
            Login objusuariomodelo = new Login();
            objusuariomodelo.setUsuario (usuario);
            objusuariomodelo.setSenha (senha);
            
            
            LoginDAO objusuariodao = new LoginDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario (objusuariomodelo);
            
            if (rsusuariodao.next()) {
                
                TelaPrincipal objtelaprincipal = new TelaPrincipal(rsusuariodao);
                objtelaprincipal.setVisible(true);
                this.dispose();
                
            } else {
                JOptionPane.showMessageDialog(null,"Usuario ou Senha Incorreto");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }//GEN-LAST:event_BTLoginActionPerformed

